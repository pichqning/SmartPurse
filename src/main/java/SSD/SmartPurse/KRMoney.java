package SSD.SmartPurse;

public class KRMoney extends Money {

    public KRMoney(int value) {
        this.value = value;
        this.currency = "Won";
    }

    @Override
    public int getValue() {
        return super.getValue();
    }

    @Override
    public String getCurrency() {
        return super.getCurrency();
    }

    @Override
    public String toString() {
        return super.toString();
    }
}

package SSD.SmartPurse;

public class CHMoney extends Money {

    public CHMoney(int value) {
        this.value = value;
        this.currency = "Yuan";
    }

    @Override
    public int getValue() {
        return super.getValue();
    }

    @Override
    public String getCurrency() {
        return super.getCurrency();
    }

    @Override
    public String toString() {
        return super.toString();
    }
}

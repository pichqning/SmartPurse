package SSD.SmartPurse;

public class UKMoney extends Money {

    public UKMoney(int value) {
        this.value = value;
        this.currency = "Pound";
    }

    @Override
    public int getValue() {
        return super.getValue();
    }

    @Override
    public String getCurrency() {
        return super.getCurrency();
    }

    @Override
    public String toString() {
        return super.toString();
    }
}
